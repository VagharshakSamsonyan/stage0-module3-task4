        // Declare variables
        int a = 5;
        int b = 3;

        // Write down the formula
        int result = (9 * a * a - 5 * b + 2 + a - 7) * ((a + b - 4 * a * b) / 2);

        // Output the result
        System.out.println("Result: " + result);
